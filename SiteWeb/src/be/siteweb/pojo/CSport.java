package be.siteweb.pojo;

public class CSport {
	private int id;
	private String name;
	
	public CSport() {}


	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
}
